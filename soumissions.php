<p>Thank you for your interest in submitting artwork for Artsida 6 and for supporting <abbr title="AIDS Community Care Montreal">ACCM</abbr>’s mission!</p>
<p>The art submission process is in two steps :</p>
<ol>
<li>Complete submission form on this website (click button below);</li>
<li>Email a high-resolution 300 <abbr title="dot per inch">dpi</abbr> <abbr title="Cyan Magenta Yellow Black">CMYK</abbr> colour image of your artwork to <a href="mailto:submissions@accmontreal.org">submissions@accmontreal.org</a>. Please put your full name and artwork title in the email subject.</li>
</ol>
<p>Submission deadline: July 1<sup>st</sup> 2015, at 11:59pm.</p>
<p>Each artist is allowed to submit a maximum of two pieces for consideration. If you are submitting two pieces, submit each piece separately using this on-line form.</p>
<p>Pieces submitted for the auction must remain available for Artsida until the artist is informed of the Curatorial Committee’s decision in early July 2015. If the piece is no longer available, the artist may submit a substitute piece but the Committee is not obligated to accept it.</p>
<p>The Curatorial Committee will set the estimated market value of submitted artwork, taking into consideration the proof of estimated value provided by the artist upon selection.</p>
<p>The artist agrees to donate their artwork to Artsida, even if the piece is not sold at or above the reserve bid price. <abbr title="AIDS Community Care Montreal">ACCM</abbr> reserves the right to try to sell the piece by other means after the main event, all the while respecting the conditions of sale as established by the artist.</p>
<p>Upon selection, the artist agrees to provide the following documentation before July 31<sup>st</sup> to Artsida:</p>
<ol>
<li>Artist Contract;</li>
<li>Proof of the estimated market value;</li>
<li>Certificate of authenticity.</li>
</ol>
<p>The artist assumes the responsibility to deliver the artwork to Artsida before July 31<sup>st</sup>.</p>
<p>Out-of-town artists must organize and cover the cost of shipping the artwork to Artsida.</p>
