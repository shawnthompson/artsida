<p>Merci de votre intérêt à présenter une œuvre pour Artsida 6 et de votre appui à la mission d’<abbr lang="en" title="AIDS Community Care Montreal">ACCM</abbr>!</p>
<p>La présentation d’une candidature comporte deux étapes :</p>
<ol>
<li>Remplissez le formulaire de candidature situé sur le site (cliquez sur le bouton ci-dessous).</li>
<li>Faites parvenir une image couleur haute résolution de 300 <abbr title="points par pouce">ppp</abbr> (<abbr title="dot per inch">dpi</abbr>) en mode <abbr title="cyan, magenta, jaune, noir">CMJN</abbr> (<abbr title="Cyan, Magenta, Yellow, Black">CMYK</abbr>) de votre œuvre à l’adresse submissions@accmontreal.org. Veuillez inclure votre nom complet et le titre de votre œuvre dans l’objet du courriel.</li>
</ol>
<p>Date limite pour la présentation des candidatures : 1er juillet 2015, à 23 h 59.</p>
<p>Les artistes peuvent soumettre deux œuvres tout au plus. Le cas échéant, les œuvres doivent être soumises séparément au moyen du formulaire en ligne.</p>
<p>Les œuvres présentées doivent rester disponibles en vue de l’encan Artsida jusqu’à ce que l’artiste ait été informé de la décision du comité de sélection au début de juillet 2015. Si l’œuvre n’est plus disponible, l’artiste peut en soumettre une autre, mais le comité n’est pas tenu de l’accepter.</p>
<p>Le comité de sélection établira la valeur marchande estimée de l’œuvre présentée en se fondant sur la preuve qu’aura fournie l’artiste à cet égard au moment de la sélection.</p>
<p>L’artiste consent à donner son œuvre à Artsida même si celle-ci n’est pas vendue à un prix égal ou supérieur au prix de départ. <abbr lang="en" title="AIDS Community Care Montreal">ACCM</abbr> se réserve le droit d’essayer de vendre l’œuvre par d’autres moyens après l’événement principal tout en respectant les conditions de vente établies par l’artiste.</p>
<p>L’artiste dont l’œuvre a été sélectionnée consent à fournir les documents suivants à Artsida avant le 31 juillet :</p>
<ol>
<li>Contrat de l’artiste</li>
<li>Preuve de la valeur marchande estimée</li>
<li>Certificat d’authenticité</li>
</ol>
<p>L’artiste s’engage à remettre son œuvre à Artsida avant le 31 juillet.</p>
<p>Les artistes qui ne résident pas à Montréal doivent faire acheminer l’œuvre à Artsida et assumer les coûts qui en découlent.</p>
