<h2><a target="_blank" href="http://entrepotsdominion.ca/">Entrepôts Dominion</a> (encan principal)</h2>
<p><a target="_blank" href="http://entrepotsdominion.ca/"><img src="img/venue-dominion.png" class="img-responsive pull-left mrgn-rght-md" alt="Entrepôts Dominion"></a>Le bâtiment des Entrepôts Dominion a reçu le prix spécial du jury Créativité Montréal en 2007 pour son design architectural remarquable. L’entreprise Merchants Manufacturing Company a entamé la construction de cet édifice historique en 1880 à l’époque où le quartier devenait l’un des centres industriels du Canada en raison de ses multiples scieries, moulins à farine, tanneries et fonderies.</p>
À proximité du Marché Atwater, facilement accessible en métro et doté d’un vaste stationnement, le bâtiment élégamment restauré est l’endroit tout indiqué pour la tenue de ce grand événement consacré à l’art.

