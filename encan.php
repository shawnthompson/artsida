<p>La sixième édition d’Artsida vise à amasser des fonds au profit d’<a href="sbm.php"><abbr lang="en" title="AIDS Community Care Montreal">ACCM</abbr> – Sida Bénévoles Montréal</a>, un organisme communautaire fondé sur l’action bénévole dont le mandat consiste à améliorer la qualité de vie des personnes vivant avec le VIH-sida, à prévenir la transmission du VIH, ainsi qu’à accroître la sensibilisation et la mobilisation du public. Pour plus de renseignements sur l’encan phare d’<abbr lang="en" title="AIDS Community Care Montreal">ACCM</abbr>, consultez la section <a href="historique.php">Historique d’Artsida</a>.</p>
